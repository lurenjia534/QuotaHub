internal fun quotaHubColorScheme(
    palette: ThemePalette,
    darkTheme: Boolean
): ColorScheme {
    return when (palette) {
        ThemePalette.QuotaHub -> if (darkTheme) QuotaHubDarkScheme else QuotaHubLightScheme
        ThemePalette.Lagoon -> if (darkTheme) LagoonDarkScheme else LagoonLightScheme
        ThemePalette.Grove -> if (darkTheme) GroveDarkScheme else GroveLightScheme
        ThemePalette.Ember -> if (darkTheme) EmberDarkScheme else EmberLightScheme
        ThemePalette.Graphite -> if (darkTheme) GraphiteDarkScheme else GraphiteLightScheme
    }
}

